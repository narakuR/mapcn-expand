export function DrawControlExample() {
  const [features, setFeatures] = useState<GeoJSON.Feature[]>(initialFeatures);

  const handleDraw = useCallback(
    (e: MapboxDraw.DrawEvent & { features: GeoJSON.Feature[] }) => {
      if (e.type === "draw.create" && e.features[0]) {
        setFeatures((prev) => [...prev, e.features[0]]);
      }
      if (e.type === "draw.delete") {
        setFeatures(prev => prev.filter(feature => !e.features.includes(feature)));
      }
      if (e.type === "draw.update") {
        setFeatures(prev => prev.map(feature => feature.id === e.features[0].id ? e.features[0] : feature));
      }
    },
    [],
  );

  return (
    <div className="relative h-full w-full">
      <MapComponent center={[-73.9857, 40.7484]} zoom={12}>
        <MapControls
          position="bottom-right"
          showDraw
          showZoom={false}
          onDraw={handleDraw}
          features={features}
        />
      </MapComponent>
    </div>
  );
}
